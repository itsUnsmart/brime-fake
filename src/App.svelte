  <h2>Site "Testing"</h2>

  <p>

  The most obvious part of this entire ordeal where it can be blatently obvious how fake it is at least to any developer
  is the site "load testing" they did. The events took place on July 7th-8th starting around 8PM and ending around 1AM central time.
  Load testing is definitely a very thing to do as you need to know that your site can handle a lot of load and not crash.
  However, any developer would know you do not test this by using a shared hosted wordpress site that is only serving static
  content to users. Static content being things that never change and can be cached on the users browser where a refresh will
  not recall the server to get an update on the file. The worst part of all of this, however, was they were using a 3$ a month
  shared hosting wordpress site from hostboogie. Shared hosting means that the Brime site was one of many sites running on the same
  server. This allows hosting companies to offer extremely cheap pricing for sites which may not receive a lot of traffic, thus not 
  needing a dedicated server to handle serving the content to users
  (<a href="https://twitter.com/lucidic/status/1280667695063732225?s=21">Here is a link to tweet proving they were using shared hosting</a>).

  </p>

